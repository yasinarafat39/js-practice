
// an array is a special variable, which can hold more than one value: 
// An array can hold many values under a single name, and you can access the value by referring to an index number. 
const cars = ["Saab", "Volvo", "BMW"];
// it's a common practice to declare array with the const keyword. 

// you can also create an array & then provide the elements: 
const cars1 = [];
cars1[0] = "Saab";
cars1[1] = "Volvo";
cars1[2] = "BMW";

console.log(cars1);


